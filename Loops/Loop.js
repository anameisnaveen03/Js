// for specific condition
let i = 1; //intilaization
while (i<=100) { //condition
    console.log(i); //statment
    i++; //inc/dec
}

// for specific range
for (let i = 1; i<=100; i++) {
    console.log(i);
}

// at least one time execution
let j = 1
do {
    console.log(j);
    j++
} while (j>100);